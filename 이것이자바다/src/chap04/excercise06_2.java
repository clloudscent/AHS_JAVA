package chap04;

public class excercise06_2 {
	 public static void main(String arg[]) {
	        for (int i = 5; i > 0; i--) {
	        //처음에 5개를 찍고 1개씩 감소하는 반복문입니다.
	            for (int j = 0; j < i; j++) {
	                System.out.print("*");
	                       //i값 개수에 따라서 별의 개수가 변하도록 합니다.
	            }
	            System.out.println();        
	        }
	 }
}
	   
	
	


