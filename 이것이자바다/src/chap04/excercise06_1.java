package chap04;

public class excercise06_1 {
		    public static void main(String arg[]) {
		        for (int i = 0; i < 5; i++) {
		          //세로 5칸에 입력할 for문을 작성합니다
		            for (int j = 0; j <= i; j++) {
		                 //별(*) 문자를 가로로 증가시키는 반복문입니다.
		                System.out.print("*");
		                        //강제개행을 하지 않고 출력시키게 하는게 포인트입니다.		                       
		            }
		            System.out.println("");        
		        }
		    }		  
		}


