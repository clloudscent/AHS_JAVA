package _december;

/*
 * System.out.println("문자열");

   System.out.println(변수명); 2개

   System.out.println("문자열"+변수명);
 */

public class _dec1 {
	public static void main(String[] args) {
		
		int number = 22;
		String str = "안녕하세요";
		
		System.out.println("Hello, World"); //문자열
		System.out.println(number); //int 변수 출력
		System.out.println(str); //String 변수 출력
		System.out.println("나는"+number+"살입니다");//문자열+변수
		                 //변수와 문자열을 '+'로만 이어주면 같이 사용할 수 있다.
	}

}
